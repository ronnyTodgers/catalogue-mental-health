                    Treatment and service use
                    </h6>
                </a>
                <div class="collapse card-body" id="faq4-1">
                    <p>There are a variety of treatments and services that exist to meet the needs of people living with mental health problems, and their use is often of interest to researchers.</p>
                    <p>Treatments for mental health problems include:</p>
                    <ul>
                    <li>Medication</li>
                    <li>Treatment from a general practitioner</li>
                    <li>Specialist treatment from a psychologist, psychiatrist, therapist, or counsellor</li>
                    <li>Care at an outpatient clinic for mental health</li>
                    <li>Inpatient care, such as in a hospital or rehabilitation centre</li>
                    </ul>
                    <p>Service use includes:</p>
                    <ul>
                    <li>Support at school, such as Special Educational Needs and Disability (SEN/SEND) services</li>
                    <li>Support at home, such as help with activities of daily living</li>
                    <li>Support in achieving personal or occupational goals, such as from a social worker</li>
                    <li>Telephone help line and online resources</li>
                    <li>Community drug and alcohol services</li>
                    <li>Contact with legal authorities, such as the police or justice system</li>
                    </ul>
                    <h6>In the Catalogue</h6>
                    <p>The <u>treatments and service use</u> included are specifically for mental health conditions.</p>
