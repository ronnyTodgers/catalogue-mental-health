<h1 class="h3 mb-4 text-gray-800">PHYSICAL HEALTH TOPICS</h1>
<div class="card faq">
	<div class="card-body">
	<p>Many of the studies on the Catalogue include measures of participants&rsquo; physical as well as mental health. Although the Catalogue cannot provide item-level information about physical health data (as it does for mental health topics), it does now provide pointers to information on the physical health of study participants collected during each sweep of data collection for every study. This information is displayed below each study's timeline in the sweep-level information section, indicated by the medical briefcase symbol <strong>( <i class="fas fa-medkit"></i> )</strong></p>
	<p>For each study, the Catalogue displays a list of physical health topics which are assessed during any particular sweep. This information includes measures of physical health conditions, symptoms and indicators of physical health collected via a range of methods, including self- and parent-report, as well as direct measurements of physical health collected by clinicians or study teams. The focus, or subject, of the physical health measures displayed also varies &ndash; these items can be about the cohort member themselves, their parents, grandparents or other relatives, and their partners. We encourage users to explore each study's datasets to discover more detailed information about the measures and data available.</p>
	<p>Below is a full list of physical health topics and their descriptions, including examples of what symptoms, conditions, and disorders are included under each topic category.</p>
