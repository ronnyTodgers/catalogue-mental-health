            <p><strong><a href="https://datamind.org.uk/our-work/courses/">DATAMIND Courses</a><br /><em>DATAMIND, the
                        Health Data Research Hub for Mental Health</em></strong></p>
            <p>DATAMIND offers short courses to support anyone from a participant to a researcher working with or
                interested in health data, especially mental health data. </p>
            <p>The courses offered include:</p>
            <ul>
                <li>The <strong><a href="https://datamind.org.uk/our-work/courses/data-literacy-short-course/">Data
                            Literacy Short Course</a></strong> (51 minutes), created by the McPin Foundation and
                    DATAMIND with the help of the DATAMIND Super Research Advisory Group. The course covers how the NHS
                    uses and stores healthcare data, patient and citizen rights, how researchers use health data to
                    learn more about health conditions and treatments, and the possible risks and benefits of using
                    data. </li>
                </p>
                <p>
                    <li><strong><a
                                href="https://datamind.org.uk/our-work/courses/a-practical-view-on-code-sharing-and-co-developing/">A
                                Practical View on Code Sharing and Co-Developing</a></strong> (16 minutes), developed by
                        DATAMIND, the UK Health Data Research Hub for Mental Health, and the University of York through
                        Project SPORE. The course covers various aspects of code sharing, including why you should
                        consider sharing your code, when to share it, with whom to share it, how to do it effectively,
                        licensing and intellectual property considerations, documenting your code, creating contributing
                        guidelines, ensuring discoverability, and code maintenance. </li>
            </ul>
