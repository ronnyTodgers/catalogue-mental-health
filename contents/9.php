            <p>Growth curve models go by a variety of names (multilevel models, mixed effects models, latent curve
                models) but share a common focus on individual change over time. In this series of videos, Patrick Bauer
                introduces <strong>growth curve modeling</strong> and provides an&nbsp;overview of many options for
                <strong>analyzing repeated measures data</strong>.&nbsp;
            </p>
            <p>Materials from an introductory workshop "<a href="https://curranbauer.org/sra2016/">Introduction to
                    Growth Curve Modeling: An Overview and Recommendations for Practice</a>" are also
                available.&nbsp;These materials include slides from the workshop and the data on Patrick describes in
                the videos, as well as scripts for fitting linear growth models to this data in SAS, SPSS, Stata, and
                Mplus.</p>
            <p>Video episodes include:</p>
            <ul>
                <li>What is growth curve modeling?</li>
                <li>The coding of time</li>
                <li>A <strong>multilevel modeling</strong> framework</li>
                <li>A <strong>structural equation modeling</strong> framework</li>
                <li>Nonlinear trajectories</li>
                <li>Time-invariant covariates</li>
                <li>Time-varying covariates</li>
                <li>Multivariate growth</li>
                <li>Choosing between multilevel and structural equation approaches&nbsp;</li>
            </ul>
