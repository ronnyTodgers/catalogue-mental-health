            <p><strong><a href="https://radiance.org.uk/">RADIANCE: Rigorous training in longitudinal data
                        science</a><br /><em>University College London</em></strong></p>
            <p>RADIANCE offers online training that aims to enhance knowledge, self-confidence and expertise among
                researchers who want to utilize complex longitudinal biosocial data.&nbsp;</p>
            <p>Topics covered include:</p>
            <ul>
                <li><strong>Data stewardship:</strong> Principles of responsible data science; data governance, ethics
                    and confidentiality; data quality assessment, accessibility, linkage; questions and targets of
                    estimation; and statistical software, data description and visualization</li>
                <li><strong>Analysis:</strong> Principles of statistical inference and modelling; data dimension
                    reduction and classification; multiple imputation of missing data​; analysis of longitudinal and
                    time to event data; machine learning methods; and causal inference</li>
                <li><strong>Reproducibility:</strong> Interpretation and triangulation; and open science, code sharing,
                    and collaborations&nbsp;</li>
            </ul>
            <p>The training program caters to a range of career stages as each topic is delivered at three levels of
                difficulty. <em><a href="https://radiance.org.uk/appetisers/">Data Science Basics</em></a> includes
                videos that are accessible online and give brief introductions to concepts and core skills. <em><a
                        href="https://radiance.org.uk/modules/">Data Science Intermediate</em></a> consists of recorded
                lectures and live online sessions with practical tutorials. <em><a
                        href="https://radiance.org.uk/courses/">Data Science Advanced Courses</em></a> are online live
                lectures and tutorials.</p>
            </ul>
