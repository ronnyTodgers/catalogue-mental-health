            <p><strong><a href="https://learning.closer.ac.uk/" target="_blank" rel="noopener">CLOSER Learning
                        Hub</a><br /><em>CLOSER</em></strong></p>
            <p>The Learning Hub is an online teaching resource has been produced by CLOSER to<strong> introduce
                    longitudinal studies to non-experts</strong> in both academic and policy settings. The Learning Hub
                has information and resources to help you explore longitudinal studies and get started using the data.
                As you progress through the different sections of the site, you can test their knowledge using
                interactive quizzes.</p>
            <p>Topics covered include:</p>
            <ul>
                <li><strong>Introduction to longitudinal research:</strong>&nbsp;types of longitudinal studies and using
                    longitudinal data for research</li>
                <li><strong>Study design:</strong>&nbsp;sampling, data collection instruments, methods of data
                    collection and sweep implementation</li>
                <li><strong>Analysis:</strong>&nbsp;getting started and exploring the data, <strong>general linear
                        regression</strong>, <strong>logistic regression</strong> and <strong>multinomial logistic
                        regression</strong></li>
            </ul>
            <p>An overview of how longitudinal research can be used by researchers, policymakers and practitioners to
                increase our understanding of mental health and wellbeing is also available in the &lsquo;explore by
                topic&rsquo; section. Case studies of longitudinal research are also provided, covering the research
                questions asked, what data were used, key findings and implications for policy.</p>
            <p>&nbsp;</p>
            <p><strong><a href="https://www.ukdataservice.ac.uk/use-data/data-skills-modules.aspx" target="_blank"
                        rel="noopener">UK Data Service Data Skills Modules</a><br /><em>UK Data
                        Service&nbsp;</em></strong></p>
            <p>These introductory level interactive modules are designed for users who want to get to grips with
                <strong>key aspects of survey, longitudinal and aggregate data</strong>. The three modules can be
                conducted in your own time and you are able to dip in and out when needed. The modules give an
                introduction to key aspects of the data using short instructional videos, interactive quizzes and
                activities using open access software where possible.
            </p>
            <p>Longitudinal data module units include:</p>
            <ul>
                <li>Unit 1: What are longitudinal data?</li>
                <li>Unit 2: Use cases</li>
                <li>Unit 3: Downloading data &amp; getting started</li>
                <li>Unit 4: Preparing to work with the data</li>
                <li>Unit 5: Analysing the data</li>
            </ul>
            <p>&nbsp;</p>
            <p><strong><a href="https://www.ncrm.ac.uk/resources/online/?catsearchquery=Quantitative%20data"
                        target="_blank" rel="noopener">NCRM Online Learning Resources</a><br /><em>National Centre for
                        Research Methods</em></strong></p>
            <p>NCRM has many online resources that cover a wide range of research methodologies and are intended to help
                people interested in <strong>social science research methods</strong>. They follow a standard format and
                each includes purposely-created video tutorials and supporting materials such as presentation slides and
                video transcripts along with related datasets, recommended readings and links to related research
                publications and resources.</p>
            <p>Topics covered include:</p>
            <ul>
                <li><strong>Survey data quality</strong> and <strong>total survey error</strong></li>
                <li><strong>Multilevel models</strong>: random coefficient and intercept models</li>
                <li><strong>Structural Equation Modelling (SEM)</strong>: what it is and what it isn't</li>
                <li>Biosocial research framework</li>
            </ul>
            <p>&nbsp;</p>
            <p><strong><a href="http://www.bristol.ac.uk/cmm/learning/online-course/" target="_blank"
                        rel="noopener">LEMMA Online Multilevel Modelling Course</a><br /><em>University of Bristol
                        Centre for Multilevel Modelling</em></strong></p>
            <p>The LEMMA (Learning Environment for Multilevel Methodology and Applications) online <strong>multilevel
                    modelling course</strong> contains a set of graduated modules starting from an introduction to
                quantitative research progressing to multilevel modelling of <strong>continuous and binary
                    data</strong>. All modules have a concept component, and most modules have practical lessons -
                instructions on how to carry out analyses in MLwiN, R and Stata. Interactive quiz questions are included
                throughout the modules to self-test your understanding.</p>
            <p>Course modules include:</p>
            <ul>
                <li><strong>Multiple regression</strong></li>
                <li><strong>Multilevel structures &amp; classifications</strong></li>
                <li>Introduction to <strong>multilevel modelling</strong></li>
                <li>Application 1: The use of performance indicators in education</li>
                <li>Regression models for binary responses</li>
                <li>Multilevel models for binary responses</li>
                <li>Multilevel modelling in practice: Research questions, data preparation and analysis</li>
                <li>Single-level and multilevel models for ordinal responses</li>
                <li>Single-level and Multilevel Models for Nominal Responses</li>
                <li>Three-level multilevel models</li>
                <li>Cross-classified multilevel models</li>
                <li>Multiple membership multilevel models</li>
                <li><strong>Missing data</strong></li>
                <li>Multilevel Modelling of Repeated Measures Data</li>
            </ul>
