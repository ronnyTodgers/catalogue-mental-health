	<a href="#faq2" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">Does the Catalogue provide information on factors that put people at risk for mental health problems?</h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq2">
		<div class="card-body">
			<p>The Catalogue does not include information on risk factors for mental health problems, as it focuses on providing detailed information about mental health and wellbeing measures.</p>
			<p>Whilst we don&rsquo;t include risk factors, many of the studies on the Catalogue do have measures of biological and social risk factors for mental health problems. To find out which studies include measures of this kind (for example, loneliness, genetic propensity, poverty) alongside mental health, you can use the &lsquo;related measures&rsquo; filters on the search page. We include this feature to narrow down your searches, but do not provide details on the measures of risk factors.</p>
		</div>
	</div>
</div>
<div class="card faq ">
	<!-- Card Header - Accordion -->
	<a href="#faq3" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">What if my search doesn’t bring up any studies?</h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq3">
		<div class="card-body">
			<p>If your initial search doesn&rsquo;t get any results &ndash; don&rsquo;t worry! We have some top tips to find exactly what you are looking for.</p>
			<p>First, it is good to try altering the terminology you use in your search or try using synonyms. Especially if you are looking for a topic that may be less commonly measured. For example, if you are looking for binge eating disorder, it is better to search for the disorder itself, rather than specific symptoms, such as &lsquo;vomiting&rsquo; or &lsquo;over-eating&rsquo;. Using different terminology will improve your search.</p>
			<p>To find the most accurate terminology to use, we recommend checking the list of mental health topics covered by the Catalogue. These are located under the &lsquo;mental health topics&rsquo; page. It is a good idea to check here if the disorder you are looking for is covered in the Catalogue before you search for it.</p>
			<p>The search engine will allow you to look for mental health and wellbeing topics, instruments, or studies only. If you are looking for related topics, you can use the filters.</p>
			<p>If you need any help, or still can&rsquo;t find what you&rsquo;re looking for, contact us at <u><a href="mailto:mentalhealth.fellow@kcl.ac.uk">mentalhealth.fellow@kcl.ac.uk</a></u>.</p>
		</div>
	</div>
</div>
