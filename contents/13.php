	<a href="#faq13" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">What makes the Catalogue unique?</h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq13">
		<div class="card-body">
		<p>There are many ways in which the Catalogue is a unique resource.</p>
		<p>The Catalogue displays information about measures of mental health and wellbeing at the item-level, providing fine-grained detail about each measure, when it was used, the response scale, focus, and informant. This allows researchers to effortlessly identify information about the studies and measures featured on the Catalogue. The detail offered by the Catalogue highlights the wealth of available longitudinal mental health and wellbeing data, enabling researchers to discover these data and find exactly what they need.</p>
		<p>The focus on UK longitudinal research on the Catalogue is significant, as these provide a rich source of data. The Catalogue maximises the use, value, and impact of longitudinal studies to help improve our understanding of mental health. You can compare data from these different studies to look at patterns of measurement across sweeps, and decide which measures are best for your research projects. Looking at longitudinal studies also enables change over time and across generations to be better understood. The UK has such a varied and wide range of longitudinal research; the Catalogue is a great place to start looking for what you need, as they are all in one place.</p>
		<p>The usability of the Catalogue also makes this resource unique. You can quickly use the search engine to look for studies, specific instruments, and mental health disorders, as well as using the filters on the side of the search page to narrow down your search. You can use these filters to easily specify your search, and find studies with specific sample characteristics, or a particular study design, for example. The measures are also presented on a searchable timeline on each study page, to show the data collection over time. You can find the measures you need, from any time point, here with ease!</p>
