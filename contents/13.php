		<h6 class="m-0 text-primary">Can I determine from the Catalogue the best measure to use for my research project?</h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq5">
		<div class="card-body">
			<p>There are strengths and possible limitations to all the measures on the Catalogue. Because of this, and the wide range of mental health topics covered, it is best for you as a researcher to decide which ones will fit the needs and aims of your project best.</p>
			<p>The Catalogue provides detail on both standard and non-standard measures, so there are many options to choose from. The Catalogue includes a range of standard instruments, like the Strengths and Difficulties Questionnaire (SDQ), which you can look for on the search page. It also has fine grained detail on each measure, both standard and non-standard. You can find these on each study&rsquo;s timeline, where you can search for specific measures and take the ones you require.</p>
			<p>To help you, there is a <a href="?content=7">&lsquo;common measures of mental health&rsquo;</a> page on the Catalogue. This will give you a summary of the most widely used standard instruments featuring on the Catalogue. We recommend using this to find details about these popular instruments. To find out more, researchers are encouraged to use the reference papers listed under each instrument.</p>
		</div>
	</div>
