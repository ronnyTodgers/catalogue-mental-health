			<p>Yes, the Catalogue includes studies using clinical cohorts. The Catalogue also covers studies with a focus on vulnerable/marginalised groups.</p>
			<p>Furthermore, the Catalogue contains a wide variety of studies and sample types, as the UK is home to many kinds of longitudinal studies and cohorts. For example, there are study designs such as household panel surveys, repeated cross-sectional surveys, and accelerated cohorts. Sample types vary as well, and the Catalogue covers a range of these, such as ageing cohorts and occupational cohorts, as well as general and clinical.</p>
			<p>To find clinical cohorts on the Catalogue, and any other sample types you need, you can use the filter &ldquo;sample characteristics&rdquo;, where you will find a full list of the types of cohorts the Catalogue covers. Also, to find different study designs, you can use the &ldquo;study design&rdquo; filter.</p>	
