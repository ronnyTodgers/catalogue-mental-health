	<a href="#faq4" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">What if a study I am searching for isn’t included on the Catalogue? </h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq4">
		<div class="card-body">
			<p>The studies on the Catalogue are those which we have focused on so far.</p>
			<p>The Catalogue has a specific inclusion criterion. To be included, studies need to:</p>
			<ul>
			<li>
			<p>Have collected/plan to collect data at multiple time points</p>
			</li>
			<li>
			<p>Include measures of mental health and/or wellbeing</p>
			</li>
			<li>
			<p>Have at least 200 participants at the first data point</p>
			</li>
			<li>
			<p>Be ongoing</p>
			</li>
			</ul>
			<p>You can find out more about the types of studies included on the Catalogue on the &lsquo;<a href="?content=1">background</a>&rsquo; and <a href="?content=2">&lsquo;gathering the information&rsquo;</a> pages.</p>
			<p>We are always on the lookout for new studies to add to the Catalogue. If you think you know a study that meets our criteria, but is not listed, please get in touch! Email us at <u><a href="mailto:mentalhealth.fellow@kcl.ac.uk">mentalhealth.fellow@kcl.ac.uk</a></u>.</p>
		</div>
	</div>
