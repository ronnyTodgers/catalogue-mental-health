            <h6 class="m-0 text-primary">Which longitudinal study designs are included? </h6>
        </a>
        <!-- Card Content - Collapse -->
        <div class="collapse" id="faq14">
            <div class="card-body">

                <p>Longitudinal research means that data is repeatedly collected over a time frame. There are multiple
                    designs or types of longitudinal studies, such as:</p>
                <ul>
                    <li>Cohort studies &ndash; Following the same group of people that share a characteristic over a
                        given period. A cohort could be a group born around the same time, share a diagnosis, or have
                        experienced the same life event. For example, the <a href="?content=study&studyid=ELSA">English
                            Longitudinal Study of Ageing (ELSA)</a>.</li>
                    <li>Household panel surveys &ndash; Data is collected repeatedly from the same household, not
                        necessarily the same people. An example of this study design is <a
                            href="?content=study&studyid=UKHLS">Understanding
                            Society</a>.</li>
                    <li>Repeated cross-sectional studies &ndash; These studies collect data from different samples at
                        each time point. This new recruitment at each wave ensures a representative sample of the
                        population. Studies with this design cannot be used to look at individuals over time but can
                        look at changes in populations. The <a href="?content=study&studyid=APMS">Adult
                            Psychiatric Morbidity Survey (APMS)</a> is an example of this design.</li>
                    <li>Accelerated cohort studies &ndash; Accelerated cohort studies follow the same design as cohort
                        studies but recruit multiple cohorts of different ages at the start of the study. An example of
                        this study design is the <a href="?content=stud&studyid=WOS">West of
                            Scotland</a></li>
                    <li>Linked cohort studies &ndash; A linked cohort is created when data is collected from multiple
                        sources of routinely collected data, such as medical records or education records. This type of
                        cohort is used in <a href="?content=study&studyid=SEP-MD">SEP-MD</a>.
                    </li>
                </ul>
