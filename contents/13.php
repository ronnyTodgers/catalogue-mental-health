		<h6 class="m-0 text-primary">What is DATAMIND? How is it linked to the Catalogue? </h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq9">
		<div class="card-body">
			<p>DATAMIND is a datahub developed by HDR UK, which works to make the best use of mental health data and enable coordinated research. It is a platform allowing researchers and others to find and use many different types of mental health data.</p>
			<p>Because DATAMIND is part of HDR UK, this hub is available through the HDR Gateway. It aims to bring together many different sources of data but is specific to mental health.</p>
			<p>The Catalogue is a part of this HDR hub. The Catalogue and DATAMIND are therefore part of a wider consortium aiming to facilitate future mental health research. The two share similar values about the importance of making mental health data discoverable, and encouraging the uptake of this data to overall improve our understanding of mental health.</p>
		</div>
