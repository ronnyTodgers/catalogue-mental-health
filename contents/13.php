	<a href="#faq8" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">What is the HDR UK Gateway and why is it linked to the Catalogue? </h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq8">
		<div class="card-body">
			<p>The Health Data Research (HDR) UK Innovation Gateway is the UK&rsquo;s National Institute for Health Data Science, bringing together many different types of health data all in one place. It is a common entry point for researchers and innovators to discover UK health-related datasets. The Gateway provides detailed information about each dataset, and these are not specific to mental health. You can find all sorts of datasets here.</p>
			<p>The Gateway does not provide access to data, but it does act as a portal to help you find health datasets that exist in the UK, so you can easily discover this data and decide if it is helpful to your research. It also provides health data resources such as research projects and publications.</p>
			<p>On some study pages of the Catalogue, you will see the HDR Gateway logo, which you can click on. This will take you to the study page on the Gateway. The Catalogue and the Gateway both provide in-depth detail about a study, just at different levels. The Catalogue provides lots of detail at item level, whilst you will find more information on broader elements such as data classes on the Gateway. The two work together to facilitate future research, and you can use both to get the most out of existing research.</p>
			<p>Head to <a href="https://www.healthdatagateway.org/">the innovation Gateway</a> to find out more!</p>
		</div>
	</div>
