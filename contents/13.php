                    specific to mental health and wellbeing only. Furthermore, maintaining this focus, the Catalogue
                    does not include measures of neurocognitive disorders, cognition, or risk factors.</p>
                <p>However, while we don&rsquo;t include detailed information about biological or social risk factors of
                    mental health measured in studies, we note these when reviewing the studies. This information can be
                    found using the &lsquo;related measures&rsquo; filter on the search page, and includes topics such
                    as loneliness and social isolation, migration and immigration, and sleep problems.</p>
                <p>Criteria for study inclusion in the Catalogue does not just depend on measures of mental health. Head
