                <p>A cohort with linked data combines primary and secondary data. For example, <strong>Born
                    </strong>i<strong>n Bradford</strong> links education records, healthcare, and environmental data to
                    their cohort information. In contrast, a linked cohort uses secondary data, such as healthcare or
                    education records, to create a dataset. An example of this is the <strong>SEP-MD </strong>study. In
                    a data linkage study, the cohort is called a &lsquo;linked cohort&rsquo;.</p>


