		<h6 class="m-0 text-primary">How can we access the data? </h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq1">
		<div class="card-body">
			<p>The Catalogue does not provide access to data. Each study on the Catalogue has its own data access policies and procedures. Some studies have their data available to download from their study site, or <a href="https://ukdataservice.ac.uk/">UK Data Service</a>. Meanwhile, other studies require that you apply on their website to use their data. Some data will be free to access, but some studies may charge a small fee. To find out more about how to access a study&rsquo;s data, head to the &lsquo;Data Access&rsquo; section of the study page.</p>
			<p>It is important to remember that the studies you see on the Catalogue may be in the process of collecting data and these will not be ready to share yet. We keep the Catalogue as up to date as possible by including the mental health measures used at the most recent sweeps.</p>
		</div>
	</div>
</div>
<div class="card faq ">
