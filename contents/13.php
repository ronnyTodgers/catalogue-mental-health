	<a href="#faq6" class="d-block card-header py-3 collapsed" data-toggle="collapse" role="button" aria-expanded="false" aria-controls="collapseCardExample">
		<h6 class="m-0 text-primary">How did you decide which measures would be listed on the Catalogue?</h6>
	</a>
	<!-- Card Content - Collapse -->
	<div class="collapse" id="faq6">
		<div class="card-body">
			<p>The Catalogue serves as a resource to give a wide view of the data already collected, maintaining a focus on mental health and wellbeing. Within this context, the Catalogue includes measures of:</p>
			<ul>
			<li>
			<p>Indicators of mental health problems, including symptoms, behaviours, and reports of diagnoses</p>
			</li>
			<li>
			<p>Impairment as a result of mental health problems</p>
			</li>
			<li>
			<p>Treatment, service use and help-seeking behaviour</p>
			</li>
			<li>
			<p>Psychological wellbeing</p>
			</li>
			</ul>
			<p>To cover all bases, the Catalogue includes standard and non-standard measures. However, these measures must be specific to mental health or wellbeing. Furthermore, maintaining this focus, the Catalogue does not include measures of neurocognitive disorders, cognition, or risk factors.</p>
			<p>Criteria for inclusion in the Catalogue does not just depend on measures of mental health. Head to the <a href="?content=1">&lsquo;background&rsquo;</a> and <a href="?content=2">&lsquo;gathering the information&rsquo;</a> pages to find out how we decide which longitudinal studies to include.</p>
		</div>
