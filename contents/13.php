			<p>You can use the filters under &lsquo;complementary data&rsquo; to identify studies that have qualitative and/or observational data. The Catalogue will not show you exactly what qualitative information has been collected, but you can easily find out more by searching for study papers, or using the study websites.</p>
			<p>If you need to find studies that include examples of qualitative measures in sweeps, you can use the search engine to find these. Just search &ldquo;qualitative measures&rdquo;.</p>
