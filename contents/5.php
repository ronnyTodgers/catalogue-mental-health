            <p>Longitudinal studies collect information repeatedly over time about the same group of people, allowing
                for analysis of change over time.&nbsp; Compared to cross-sectional research, longitudinal designs have
                a number of significant advantages, including allowing for examination of patterns of change and the
                influence of earlier life circumstances on later outcomes. Longitudinal research can also provide
                insight into causal mechanisms and processes that is beyond the scope of cross-sectional studies.
                According to CLOSER, the UK is home the wold&rsquo;s largest and longest-running portfolio of
                longitudinal studies, with studies featuring large sample sizes, wide breadths of measures and
                exceptionally long follow-up times.</p>
