            <h4>Types of longitudinal studies</h4>
            <p>Longitudinal study design can vary depending on the priorities and goals of researchers, and choosing the
                appropriate study for your research will depend on your research question and the data available. While
                longitudinal study designs can vary on a wide range of factors including sampling units, time scale and
                research focus, four broad types of studies are described here.</p>
            <ul>
                <li><strong>Cohort studies</strong> <br />Cohort studies follow the lives of a population subgroup that
                    has experienced the same life event during a given time period (a cohort). The most well-known
                    examples of this type of study are birth cohort studies which follow groups of people born within
                    the same time period. The 1958 and 1970 British Birth Cohorts, for example, recruited a random
                    sample from all children born in a particular week in the UK. Examples of non-birth cohorts include
                    people working for a particular organisation at one time, such as the Whitehall Study, or a
                    population studying at school at a given time, such as the Next Steps or REACH studies. Cohort
                    studies are often set up to observe long-term change and individual developmental processes and
                    focus on collecting information about specified individual cohort members. Some long-running cohort
                    studies also collect information about cohort members&rsquo; parents, families and
                    children.<br /><br /></li>
                <li><strong>Household panel surveys <br /></strong>Rather than focusing on collecting data over time
                    about a sample of individuals, household panel surveys collect information about the household at
                    each wave. Household panel surveys generally collect information about every member of a household
                    as well as information about the household itself, such as the type of dwelling, housing costs and
                    the relationship between the household members. These studies generally aim to remain representative
                    of the population of households as a whole, requiring a more complicated design than cohort studies,
                    and often include complex following-rules so that new entrants, individuals and families can be
                    incorporated into the sample at each wave. Understanding Society, The UK Household Longitudinal
                    Survey is an example of a household panel survey.<br /><br /></li>
                <li><strong>Repeated cross-sectional studies <br /></strong>Some cross-sectional studies are repeated at
                    regular intervals and include a large number of repeat questions at each data collection point.
                    While studies of this type are not longitudinal because they collect data from different samples at
                    each time point, the use of repeated measures allows researchers to examine population or group
                    change over time. For this reason, a number of repeat cross-sectional studies are included in the
                    catalogue. The recruitment of a new sample at each data collection point allows these studies to
                    observe a representative sample at each wave. If representative samples are present in multiple
                    waves, population changes in behaviour or circumstances can be examined using repeated
                    cross-sectional data. Repeated cross-sectional surveys cannot be used to look at individual change,
                    however. The European Social Survey, which biennially recruits and interviews fresh representative
                    samples from over 20 European countries, is an example of a repeated cross-sectional
                    survey.<br /><br /></li>
                <li><strong>Accelerated cohort studies <br /></strong>Most age-based cohort studies study just a single
                    cohort of individuals over time. In contrast, accelerated longitudinal designs recruit multiple
                    cohorts, with each cohort entering the study at a different age. This approach has the advantage of
                    allowing a study to span the age range researchers are interested in within a shorter period of time
                    than would be possible using a single cohort design. As each participant&rsquo;s measurement
                    schedule covers only part of the age range of interest, a disadvantage of this approach is that data
                    will not be available for all participants at all ages.</li> <br>
                <li><strong>Linked cohort studies</strong><br>A dataset composed of routinely collected or
                    administrative data, such as medical or census records. The data relating to a group of individuals
                    are linked together to follow them over time. An example of a linked cohort on the Catalogue is the
                    Social and Economic Predictors of the Severe Mental Disorders Study (SEP-MD), which connects
                    individual census data to clinical records and death registrations.
            </ul>
