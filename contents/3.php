<p>Once you have identified a study that has collected data you are interested in, click the study name to navigate to the study page. This page provides information about the study&rsquo;s aims, sample and data access policies. The mental health measures timeline at the bottom of the page provides information about the study sweeps/participant ages at which mental health and wellbeing measures were collected, with each dot on the timeline representing a data collection point. Here you can browse the measures collected at each point by clicking on the dot or search for a particular mental health or wellbeing topic or instrument in the search bar. Sweeps with measures that match your search terms will be indicated with a tree icon.</p>
<p>When you have found a measure you are interested in, click the plus sign to get more information about the:</p>
<ul>
<li>Items</li>
<li>Response scale</li>
<li>Reporting period</li>
</ul>

<p>Below each study timeline, you will see the &apos;sweep level information&apos; section which displays information the particular sweep, such as the sweep name, the age of the cohort members, and the data collection period. Here you can also see any physical health measures collected during an assessment.</p>

