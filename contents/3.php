<h1 class="h3 mb-4 text-gray-800">How to use the Catalogue</h1>
<div class="card mb-4">
<div class="card-body">
<p>The purpose of the Catalogue is to provide easy access to information about mental health and wellbeing measures collected in British cohort and longitudinal studies. It provides details of the measures and studies and is designed to serve as a resource for researchers seeking data that include mental health and wellbeing measures; planning harmonisation studies; or planning further data collection.</p>
<h4>Using the Catalogue</h4>
<p>The Catalogue can be browsed in a number of different ways depending on your interests and needs. You can search the Catalogue for:</p>
<ul>
<li><strong>Mental health or wellbeing topics.</strong> For example, if you are looking for a study that has measures of depression, you could search &lsquo;depression&rsquo;.</li>
<li><strong>Particular mental health measures.</strong> For example, if you know you are interested in the General Health Questionnaire, you could search for &lsquo;GHQ&rsquo; or '"general health questionnaire"'.</li>
<li><strong>Other areas of interest.</strong> This is particularly useful if you wish to examine the association between mental health and another topic you are interested in. In this case, you can use the filters on the left of the search page to filter for studies that have collected information about themes such as &lsquo;ethnicity and race&rsquo;, &lsquo;diet and nutrition&rsquo;, &lsquo;physical health&rsquo; or &lsquo;education&rsquo;.&nbsp;</li>
<li><strong>Study design.&nbsp;</strong>In this case you could search for &lsquo;birth cohort&rsquo;, &lsquo;twin&rsquo; or &lsquo;repeat cross-sectional&rsquo; studies.</li>
<li><strong>Study name. </strong>If you already know which study you are interested in, you can also search for the study name.</li>
</ul>
<p>Search terms can be combined to narrow the list of studies down to those which meet your needs.</p>
