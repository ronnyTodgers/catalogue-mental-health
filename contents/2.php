<p>The Catalogue of Mental Health Measures is based on a review of the mental health and wellbeing measures in UK cohort and longitudinal studies. Here we give an overview of studies that met inclusion criteria, what information can be found in the Catalogue and how this information was collected.</p>
<h4>Cohort and longitudinal studies included in the review</h4>
<p>The UK is home to the world&rsquo;s largest and longest-running portfolio of longitudinal studies, which feature large sample sizes, wide breadths of measures and exceptionally long follow-up times. The Catalogue focuses on studies that met five main criteria. To be included in the Catalogue, studies need to:</p>
<ul>
<li><strong>Have collected, or plan to collect, data at multiple time points.</strong>Most studies in the Catalogue are cohort or household panel studies. This means that data were collected about the same individuals or households over time. A small number of large repeated cross-sectional studies, which collected information from a new sample at each time point, have also been included because of their value to mental health research.</li>
<li><strong>Include measures of mental health or wellbeing.</strong></li>
<li><strong>Have at least 200 participants at the first data collection point.</strong></li>
<li><strong>Have collected data in the UK.</strong>Most studies in the Catalogue have an exclusively UK-based sample. A small number of studies have included international samples in addition to a British sample.</li>
<li><strong>Be ongoing</strong>. Studies must have collected data in the last two years, be currently collecting data or have firm plans to collect new data to be included at this stage.</li>
</ul>
<p>We did not include studies that focus on a specific physical health problem such as cancer or diabetes but met our inclusion criteria at this stage. This is to ensure that we have focused on studies that are most likely to be useful to users from a wide range of disciplines interested in conducting research about mental health.&nbsp;</p>
