            <p><strong>DATAMIND Super Research Advisory Group (SRAG)</strong></p>
            <p>From the outset, DATAMIND agreed to “work with patients and people with personal experience
                of mental illness to understand whom they trust to use their data, and to develop ways
                people can work together on mental health.”</p>
            <p>As a result, the Super Research Advisory Group (SRAG) was developed by DATAMIND to engage
                with members of their communities and the general public. The SRAG comprises people with
                personal experiences of mental illness, whether personal or through caring for others.</p>
            <p>Some projects that the SRAG have worked on include:</p>
            <p>
            <ul>
                <li>Developing a user-friendly mental health DATAMIND glossary </li>
                <li>Creating a diagram for ‘Meaningful Involvement of Patients, Carers, and the Public in
                    Research’</li>
                <li>Supporting the simplification of genetic tables for patients and the public</li>
                <li>Sharing discovered insights at the MQ Data Science Conference</li>
            </ul>
            <p>The SRAG works closest with the projects of ‘Public Participation, Partnership and
                Governance’ but members of the SRAG are continuously contributing to other projects
                affiliated with DATAMIND as well.</p>
