            <h4>NEW STUDIES & COLLABORATIVE INITIATIVES ON MENTAL HEALTH & COVID-19</h4>
            <p>
                A number of new studies were initiated to explore the effects of the pandemic on mental health, such as
                the <a target="_blank" href="https://cosmostudy.uk/">Covid Social Mobility and Opportunities (COSMO)
                    Study</a></li>. Many of these studies coordinated efforts to facilitate collaboration between them.
            </p>
            <p>
                <b>WELLCOME TRUST Covid-19 QUESTIONNAIRE</b><br>
                The Wellcome Longitudinal Population Study (LPS) Covid-19 Steering Group & Secretariat developed the <a
                    href=“https://www.bristol.ac.uk/alspac/covid-19/wellcome-covid-19/” target=“_blank”>Wellcome Trust
                    Covid-19 Questionnaire</a> to maximise the benefits gained from pandemic data collection by existing
                cohorts at minimal added cost and maximal public benefit.<br><br>
                The questionnaire is <b>freely available to all population health researchers</b> wishing to address
                health, behaviour, social, environmental and economic questions in the context of the pandemic. Find out
                more on the <a href=“https://www.bristol.ac.uk/alspac/covid-19/wellcome-covid-19/”
                    target=“_blank”>questionnaire webpage</a>. Search <b>“Wellcome”</b> in the Covid-19 timeline to
                identify data collection points that have utilised this Survey.
            </p>
            <p>
                <b> NIH CORONAVIRUS HEALTH IMPACT (CRISIS) SURVEY</b><br>
                The <a href="http://www.crisissurvey.org/" target="_blank">CRISIS Survey</a> was designed to enable
                researchers and care providers to examine the extent and impact of life changes induced by the epidemic
                on the mental health and behaviour of individuals and families across diverse international settings.
                The survey is available for download and use on the <a href=” http://www.crisissurvey.org/”
                    target=“_blank”>CRISIS website</a>. Search <b>“crisis”</b> in the Covid timeline to identify data
                collection points that have utlitsed the CRISIS Survey.
            </p>
            <p>
                <b>COVID MINDS</b><br>
                <a href="https://www.covidminds.org/" target="_blank">COVID-MINDS</a> is a network of longitudinal
                studies on the global mental health impact of Covid-19 funded by the Wellcome Trust. Covid-MINDS aims to
                support data sharing, the harmonisation of mental health measures, and dissemination. The network have
                identified:
            <ul>
                <li>New and established <b>longitudinal studies</b> examining mental health during Covid-19</li>
                <li>Studies based in the UK and <b>across the world</b></li>
            </ul>
            This <a href="https://www.covidminds.org/longitudinal-studies" target="_blank">list of studies</a> is a
            useful resource to identify Covid-19 studies. If you ran a relevant longitudinal study, you can register
            your study with the network.
            </p>
            <p>
                <b>COVID-19 & MENTAL HEALTH STUDIES REGISTER</b><br>
                The NIHR Mental Health Translational Research Collaboration (MH-TRC) established the <b>Covid-19 and
                    Mental Health Studies Register</b> with the aim of facilitating the development of high-quality
                collaborative mental health research in the context of the pandemic. The register collated information
                about:
            <ul>
                <li><b>Any study based in the UK</b> that either <b>addresses mental health</b> aspects of the Covid-19
                    pandemic, or has the potential to address this field (for example, a Covid-19 respiratory study
                    which is open to adding mental health outcome measures)</li>
                <li>International studies which have UK partners</li>
            </ul>
            </p>
            <p>
                <b>UK LONGITUDINAL LINKAGE COLLABORATION (LLC)</b><br>
                The <a href="https://ukllc.ac.uk/" target="_blank">UK LONGITUDINAL LINKAGE COLLABORATION</a> (UK LLC) is
                a Trusted Research Environment (TRE) that hosts longitudinal study data, including mental health data,
                from 20 of the UK&rsquo;s established <a href="https://ukllc.ac.uk/partner-studies/"
                    target="_blank">longitudinal studies</a> that is linked to studies&rsquo; participants NHS health
                records. It is a unique four-nations infrastructure for secure cross-cohort analysis where researchers
                can apply to access linked data across all partner studies through a single application.<br><br>
                The UK LLC TRE contains pre-pandemic longitudinal data and data collected from participants during the
                lockdowns and throughout the pandemic. ONS Approved Researchers working on Covid-19 research may apply
                to access this data, which is currently free of charge.
            </p>



