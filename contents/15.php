					<p>There are a number of different definitions of <strong><em>temperament </em></strong>(in part depending on the age at which it is assessed), and a range of instruments have been designed to assess aspects of temperament in infancy and childhood. We have included all standard instruments of temperament on the Catalogue, and used these to identify non-standard items as well. Examples of these include:</p>
			<p>Infant Behaviour Questionnaire (3-12 months):</p>
			<ul>
			<li><strong>Activity level</strong>: movement of arms and legs, squirming and locomotor activity.</li>
			<li><strong>Distress to limitations</strong>: fussing, crying, or showing distress when in a confining place or position.</li>
			<li><strong>Soothability</strong>: the extent to which the infant/child&rsquo;s fussing, crying, or distress reduces when the caretaker uses soothing techniques.</li>
			</ul>
			<p>Early Childhood Questionnaire (18-36 months):</p>
			<ul>
			<li><strong>Activity/energy level</strong>: the rate and intensity of gross motor activity.</li>
			<li><strong>Fear</strong>: negative affect, including unease, worry or nervousness related to anticipated distress or perceived threatening situations.</li>
			<li><strong>Shyness</strong>: slow or inhibited approach and/or discomfort in social situations involving novelty or uncertainty; versus sociability, for example, seeking and taking pleasure in interactions with others.</li>
			</ul>
			<p>Children&rsquo;s Behaviour Questionnaire (3-7 years):</p>
			<ul>
			<li><strong>Inhibitory control</strong>: the capacity to plan and to suppress inappropriate responses in novel or uncertain situations.</li>
			<li><strong>Sadness</strong>: the amount of negative affect and lowered mood and energy related to exposure to suffering, disappointment, and loss.</li>
			<li><strong>Anger/frustration</strong>: the amount of negative affect expressed when tasks are interrupted or goals are blocked.</li>
			</ul>
			<p>The Emotionality, Activity and Sociability (EAS) Scale measures similar temperamental dimensions as those listed above, and we therefore also considered this instrument&rsquo;s dimensions when identifying non-standard items from studies across the Catalogue. The EAS is designed to identify heritable temperamental traits at any age but is generally used during childhood.</p>
			<p>The EAS establishes three main dimensions of temperament:</p>
			<ul>
			<li><strong>Emotionality</strong>: an individual&rsquo;s emotional reaction to environmental stimuli.</li>
			<li><strong>Activity</strong>: an individual&rsquo;s level of energy.</li>
			<li><strong>Sociability</strong>: an individual&rsquo;s level of comfort around, and interaction with, other people.</li>
			</ul>
			<p>This is not an exhaustive list but demonstrates the types of measures and dimensions of temperament included on the Catalogue.&nbsp;</p>

