		<div class="card-body">
		<p>Personality can be measured using standard instruments such as:</p>
			<ul>
			<li>Big Five Inventory (BFI)</li>
			<li>International Personality Item Pool (IPIP)</li>
			<li>Midlife Development Inventory</li>
			<li>Eysenck&rsquo;s Personality Inventory (EPI)</li>
			</ul>
			<p>Standard measurements of <strong><em>personality</em></strong> are mostly based on the widely supported dimensions of personality known as the <strong>OCEAN </strong>traits, the &lsquo;Big Five&rsquo;, or the &lsquo;Five Factor Model&rsquo;. These are:</p>
			<ul>
			<li><strong>Openness</strong>: refers to the willingness to try new things as well as to engage in imaginative and intellectual activities. It includes the ability to &ldquo;think outside of the box.&rdquo; Open people will enjoy learning new things, be curious and have a wide range of interests. A more closed person may dislike change, be less imaginative and resist new ideas.</li>
			<li><strong>Conscientiousness</strong>: refers to a person&rsquo;s ability to regulate their impulse control to engage in goal-directed behaviours. It measures elements such as control, inhibition, and persistence of behaviour. Individuals scoring highly on conscientiousness are more likely to be organised, hardworking, and goal driven, and those with a low score may be more impulsive, messy, and perhaps irrational.</li>
			<li><strong>Extraversion</strong>: this reflects the extent of and intensity with which individuals seek interaction with their environment, particularly socially. It encompasses the comfort and assertiveness levels of people in social situations. Extraverts will be more likely to seek excitement, enjoy being around others, and be more outgoing; introverts may avoid large groups and situations that involve making small talk.</li>
			<li><strong>Agreeableness</strong>: this focuses on people&rsquo;s orientation and interactions with others, and how kind and helpful they are. It encompasses trust, altruism, and prosocial behaviours. Those scoring highly tend to be more cooperative and compassionate; those scoring low on agreeableness may take little interest in others or be more competitive and stubborn.</li>
			<li><strong>Neuroticism</strong>: refers to the overall emotional stability of an individual. It considers how likely a person is to interpret events as threatening or difficult. It is characterised by sadness, moodiness, and emotional instability. By contrast, individuals who score low on neuroticism tend to be more relaxed, less emotionally reactive, and more optimistic.</li>
			</ul>
