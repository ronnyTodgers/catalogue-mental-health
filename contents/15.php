<h1 class="h3 mb-4 text-gray-800">Personality and temperament</h1>
<div class="card mb-4">
<div class="card-body">
<p>Alongside measures of mental health problems, the Catalogue details measures of personality and temperament, at the item level. While personality and temperament are not mental health <em>problems</em>, there can be links between personality features and vulnerability or resilience to mental health difficulties.</p>
<p>We have used the following definitions to guide our selection of the personality and temperament measures to be included in the Catalogue:</p>
<p><strong>Personality: </strong>the combination of characteristics and qualities that form an individual&rsquo;s distinctive character, which are expressed in consistent patterns of thinking, emotions, actions and behaving. Personality is most commonly assessed in adults and is most clearly expressed in interactions with other people, the environment and in social situations. Personality is thought to be formed from a combination of genetic predispositions and experiences, and shows some stability across the life course.</p>
<p><strong>Temperament: </strong>also relates to consistent individual differences in behaviour, especially in terms of reactivity and self-regulation. Temperament is most often assessed in childhood, and variations in temperament are often recognisable within the first few weeks of life. Like personality, temperamental traits are partly heritable, and may sometimes be early indications of personality.</p>
<p>There has been extensive debate over the extent to which personality and temperament overlap, or are separate constructs. We consider them together under the same topic heading on the Catalogue. On the Catalogue, you can find items relating to personality and/or temperament under the topic heading &lsquo;Personality and temperament&rsquo;, or by searching for the standard instruments and non-standard items listed below.</p>
<p>Sometimes, there will be overlap between personality and temperament items and mental health topics included on the Catalogue. For example, measures of impulsivity appear under personality and temperament, but also ADHD. <strong><em>Personality disorders</em></strong> are <u>not</u> considered part of personality and temperament, and are listed under a separate topic heading on the Catalogue.</p>
